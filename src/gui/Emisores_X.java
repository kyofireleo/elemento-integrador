    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        Connection con = Elemento.odbc();
        Statement stmt = factory.stmtEscritura(con);
        try{
            String query, textoLog;
            if(modificar){
                query = "UPDATE Emisores SET nombre=\'"+nombre.getText().trim()+"\',rfc=\'"+rfc.getText().trim()+"\',calle=\'"+calle.getText().trim()+"\',"
                        + "noExterior=\'"+noExterior.getText().trim()+"\',noInterior=\'"+noInterior.getText().trim()+"\',colonia=\'"+colonia.getText().trim()+"\',"
                        + "localidad=\'"+localidad.getText().trim()+"\',municipio=\'"+municipio.getText().trim()+"\',estado=\'"+estado.getSelectedItem().toString().trim()+"\',"
                        + "pais=\'"+pais.getSelectedItem().toString().trim()+"\',cp=\'"+cp.getText().trim()+"\',pass=\'"+String.copyValueOf(pass.getPassword()).trim()+ "\',"
                        + "email=\'"+ email.getText().trim() +"\' WHERE id="+id;
                textoLog = "El emisor " + nombre.getText() + " fue actualizado correctamente";
            }else{
                query = "INSERT INTO Emisores (nombre,rfc,calle,noExterior,noInterior,colonia,localidad,municipio,estado,pais,cp,pass,email) "
                    + "VALUES (\'"+nombre.getText().trim()+"\',\'"+rfc.getText().trim()+"\',\'"+calle.getText().trim()+"\',\'"+noExterior.getText().trim()+"\',"
                    + "\'"+noInterior.getText().trim()+"\',\'"+colonia.getText().trim()+"\',\'"+localidad.getText().trim()+"\',\'"+municipio.getText().trim()+"\',"
                    + "\'"+estado.getSelectedItem().toString().trim()+"\',\'"+pais.getSelectedItem().toString().trim()+"\',\'"+cp.getText().trim()+"\',\'" + String.copyValueOf(pass.getPassword()).trim() + "\',"
                    + "\'"+ email.getText().trim() +"\')";
                textoLog = "El emisor " + nombre.getText() + " fue agregado correctamente";
            }
            stmt.execute(query);
            JOptionPane.showMessageDialog(null, textoLog);
            Elemento.log.info(textoLog);
            stmt.close();
            con.close();
            this.dispose();
        }catch(SQLException e){
            e.printStackTrace();
            String msg = "Excepcion: No se pudo agregar al emisor a la base de datos: " + e.getLocalizedMessage();
            Elemento.log.error(msg,e);
            JOptionPane.showMessageDialog(null, msg);
        }catch(Error err){
            err.printStackTrace();
            String msg = "Error: No se pudo agregar al emisor a la base de datos: " + err.getLocalizedMessage();
            Elemento.log.error(msg,err);
            JOptionPane.showMessageDialog(null, msg);
        }
    }//GEN-LAST:event_guardarActionPerformed

