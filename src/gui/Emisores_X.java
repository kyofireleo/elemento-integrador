    private void borrarEmisor(){
        Connection con = Elemento.odbc();
        Statement stmt = factory.stmtEscritura(con);
        
        try{
            stmt.executeUpdate("DELETE FROM Emisores WHERE id = "+id);
            JOptionPane.showMessageDialog(null, "El Emisor " + nombre.getText() + " fue eliminado correctamente");
            stmt.close();
            con.close();
            this.dispose();
        }catch(SQLException | HeadlessException ex){
            ex.printStackTrace();
            Elemento.log.error("Excepcion al eliminar un emisor: " + ex.getMessage(),ex);
            JOptionPane.showMessageDialog(null, "Excepcion al eliminar un emisor: " + ex.getMessage());
        }
    }
    
