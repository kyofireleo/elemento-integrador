    private void paisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paisActionPerformed
        // TODO add your handling code here:
        DefaultComboBoxModel combo = (DefaultComboBoxModel) estado.getModel();
        
        if(listaEstados.isEmpty()){
            for (int i = 0; i < combo.getSize(); i++) {
                listaEstados.add(combo.getElementAt(i).toString());
            }
        }
        
        if(pais.getSelectedIndex() > 0){
            combo.removeAllElements();
            combo.addElement("Estado Extranjero");
        }else{
            combo.removeAllElements();
            for (int i = 0; i < listaEstados.size(); i++) {
                combo.addElement(listaEstados.get(i));
            }
        }
    }//GEN-LAST:event_paisActionPerformed

